    private void btnGuardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarProductoActionPerformed
        try {
            String nombre = txtNombreProducto.getText();
            int cantidad = Integer.parseInt(txtCantidadFabricada.getText());
            double costo = Double.parseDouble(txtCostoFabricacion.getText());

            Producto p = new Producto(idProductoActual, nombre, cantidad, costo);
            listaProductos.add(p);

            idProductoActual++;
            lblProductoId.setText(String.valueOf(idProductoActual));

            txtNombreProducto.setText("");
            txtCantidadFabricada.setText("");
            txtCostoFabricacion.setText("");

            actualizarComboProductos();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingresa datos válidos.");
        }
    }//GEN-LAST:event_btnGuardarProductoActionPerformed

    private void btnGuardarDetalleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarDetalleActionPerformed
        if (cboProductos.getSelectedIndex() == -1 || cboMateriaPrima.getSelectedIndex() == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un producto y una materia prima.");
            return;
        }

        try {
            int cantidadUsada = Integer.parseInt(txtCantidadUsada.getText());

            // Producto seleccionado
            String productoSeleccionado = (String) cboProductos.getSelectedItem();
            int idProducto = Integer.parseInt(productoSeleccionado.split(" - ")[0]);
            Producto producto = listaProductos.stream()
                    .filter(p -> p.getIdProducto() == idProducto)
                    .findFirst()
                    .orElse(null);

            // Materia prima seleccionada
            String materiaSeleccionada = (String) cboMateriaPrima.getSelectedItem();
            int idMateria = Integer.parseInt(materiaSeleccionada.split(" - ")[0]);
            MateriaPrima materiaPrima = listaMateriasPrimas.stream()
                    .filter(m -> m.getIdMateriaPrima() == idMateria)
                    .findFirst()
                    .orElse(null);

            if (producto != null && materiaPrima != null) {
                DetalleDeProducto detalle = new DetalleDeProducto(producto, materiaPrima, cantidadUsada);
                listaDetalles.add(detalle);
                actualizarTablaDetalles();
                txtCantidadUsada.setText("");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingresa una cantidad válida.");
        }
    }//GEN-LAST:event_btnGuardarDetalleActionPerformed

    private void btnGuardarMateriaPrimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMateriaPrimaActionPerformed
        String nombre = txtNombreMateriaPrima.getText();

        if (nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El nombre no puede estar vacío.");
            return;
        }

        MateriaPrima m = new MateriaPrima(idMateriaPrimaActual, nombre);
        listaMateriasPrimas.add(m);

        idMateriaPrimaActual++;
        lblMateriaPrimaID.setText(String.valueOf(idMateriaPrimaActual));
        txtNombreMateriaPrima.setText("");

        actualizarComboMateriasPrimas();
    }//GEN-LAST:event_btnGuardarMateriaPrimaActionPerformed

