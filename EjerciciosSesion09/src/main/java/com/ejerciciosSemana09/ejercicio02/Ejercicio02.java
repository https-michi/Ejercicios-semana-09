    private void actualizarTablaDetalles() {
        String[] columnas = {"Producto", "Materia Prima", "Cantidad usada"};
        Object[][] data = new Object[listaDetalles.size()][3];

        for (int i = 0; i < listaDetalles.size(); i++) {
            DetalleDeProducto d = listaDetalles.get(i);
            data[i][0] = d.getProducto().getNombre();
            data[i][1] = d.getMateriaPrima().getNombre();
            data[i][2] = d.getCantidadUsada();
        }

        tblDetalle.setModel(new javax.swing.table.DefaultTableModel(data, columnas));
    }

